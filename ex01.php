<?php
$n = 1;

// Imprimindo 1 a 10 usando while
echo "Imprimindo usando WHILE<br>";
while($n <= 10){
    echo $n . "<br>";
    $n++;
}

// Imprimindo 1 a 10 usando do-while
echo "Imprimindo usando DO-WHILE<br>";
$n = 1;
do{
    echo $n . "<br>";
    $n++;
}while($n <= 10);

// Imprimindo 1 a 10 usando for
echo "Imprimindo usando FOR<br>";
for($n = 1; $n <= 10; $n++){
    echo $n . "<br>";
}



?>


